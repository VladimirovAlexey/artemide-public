    s1=s1+alpha**3*(&
      -297029d0/27d0 + (8816d0*zeta2)/3d0 + Lmu**3*(242d0 - (88d0*Nf)/3d0 + (8d0*Nf**2)/9d0 + 864d0*zeta2) &
